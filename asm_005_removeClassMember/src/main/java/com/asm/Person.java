package com.asm;

public class Person {
	
	private String name;
	private int age;
	private int sex;
	
	
	public String getName() {
		
		return name;
		
	}
	
	public int getAge() {
		
		return age;
	}
	
	
	public int getSex() {
		
		return sex;
		
	}
	
	public void setName(String name) {
		
		this.name = name;
		
	}
	
	public void setAge (int age) {
		
		
		this.age = age;
	}
	
	
	public void setSex(int sex) {
		
		this.sex = sex;
	}
	
}
